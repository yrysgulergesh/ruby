ат = "Айбек" # Текст (String) - тексттик маанини сактоо үчүн колдонулат.
жаш = 25 # Сан (Integer) - бүтүн санды сактоо үчүн колдонулат.
бийиктик = 1.75 # Калкып жүргөн чекиттүү сан (Float) - бөлчөк санды сактоо үчүн колдонулат.
үйлөнгөнбү = false # Логикалык маанилер (Boolean) - true же false маанилерин сактоо үчүн колдонулат.

# Текст (String) - булар символдордун тизмеги. Тексттер кош тырмакча ("") же бир тырмакча ('') менен түзүлөт.
салам = "Салам, Дүйнө!"
# Тексттерди бириктирүү үчүн '+' оператору колдонулат.
толук_ат = ат + " " + "Кожомкулов" # "Айбек Кожомкулов"

# Интерполяция (Interpolation) - бул тексттин ичине өзгөрмөлөрдүн маанисин кошуу ыкмасы.
саламдашуу = "Менин атым #{ат} жана мен #{жаш} жаштамын." # "Менин атым Айбек жана мен 25 жаштамын."

# Сан (Integer) - бүтүн сандарды көрсөтөт. Арифметикалык операциялар менен иштөө.
сан1 = 10
сан2 = 3
кошуу = сан1 + сан2 # 13
чегерүү = сан1 - сан2 # 7
көбөйтүү = сан1 * сан2 # 30
бөлүү = сан1 / сан2.to_f # 3.3333333333333335 (бөлүүдө так жыйынтык алуу үчүн санды float кылуу керек)

# Логикалык маанилер (Boolean) - true же false маанилерин сактоо үчүн колдонулат.
жашы_чон = жаш > 18 # true
ат_туура = (ат == "Айбек") # true
жаш_жокпу = (жаш != 30) # true

# Массивдер (Arrays) - булар бир нече маанилерди бир жерде сактоочу структуралар.
аттар = ["Айбек", "Бектур", "Сулайман"]
сандар = [1, 2, 3, 4, 5]
биринчи_ат = аттар[0] # "Айбек" (индекс нөлдөн башталат)
экинчи_сан = сандар[1] # 2

# Массивдин методдору
сандар.push(6) # массивге элемент кошуу [1, 2, 3, 4, 5, 6]
сандар.delete_at(2) # массивден элементти алып салуу [1, 2, 4, 5, 6]

# Хэштер (Hashes) - булар ачкыч-баалу жуптар (key-value pairs) менен маалыматтарды сактоочу структуралар.
адам = { ат: "Айбек", жаш: 25, шаар: "Бишкек" }
шаар = адам[:шаар] # "Бишкек"
адам[:телефон] = "123-456-7890" # жаңы ачкыч-баалу жуп кошуу
адам.delete(:жаш) # ачкыч-баалу жупту алып салуу {:ат=>"Айбек", :шаар=>"Бишкек", :телефон=>"123-456-7890"}

# Массив жана Хэш менен иштөө
аттар.each do |ат|
  puts "Салам, #{ат}!"
end

автор = { ат: "Чыңгыз", фамилия: "Айтматов", чыгармалар: ["Жамийла", "Ак кеменин сыры", "Гүлсарат"] }
автор.each do |ачкыч, баа|
  puts "#{ачкыч.capitalize}: #{баа.is_a?(Array) ? баа.join(', ') : баа}"
end

# Жыйынтык маалыматтарды консольго чыгаруу
puts "Менин атым #{ат}, мен #{жаш} жаштамын."
puts "Мен #{шаар} шаарында жашайм жана менин боюм #{бийиктик} метр."
puts "Үйлөнгөнбү? #{үйлөнгөнбү ? 'Ооба' : 'Жок'}"
